'''
Make a function to consume a list of integers and then 
return the first and last values in a new list

Example usage:
print(firstLast([1, 2, 3, 4])) 
Output: [1, 4]
'''

# Write your code here





































# Solution

'''
def firstLast(nums: List[int]) -> List[int]:
    if not nums:
        return []
    return [nums[0], nums[-1]]
'''
