y = "Hello World!"					             # string
print(y)

y = 10							                 # integer
print(y)

y = 15.5						                 # float
print(y)

y = False						                 # boolean
print(y)

y = ["pizza", "berries"]				         # list
print(y)

y = ("mango", "orange")				             # tuple
print(y)

y = {"fruit" : "mango", "type" : "sweet"}	     # dictionary
print(y)

y = {"apple", "berry", "mango"}			         # set
print(y)

y = None	                                     # NoneType
print(y)